from http.server import HTTPServer
from pysimplesoap.server import SoapDispatcher, SOAPHandler



def sumar(a,b):
    c=a+b
    return "la suma de {}+{} es , {}".format(a,b,c)

def resta( a, b):
    c=a-b
    return "la resta de {}-{} es , {}".format(a,b,c)

def multiplicacion( a, b):
    c=a*b
    return "la multiplicacion de {}*{} es , {}".format(a,b,c)

def division( a, b):
    c=a/b
    return "la division de {}/{} es , {}".format(a,b,c)





dispatcher = SoapDispatcher(
    "soap-server",
    location="http://localhost:8000/",
    action="http://localhost:8000/",
    namespace="http://localhost:8000/",
    trace=True,
    ns=True,
)



dispatcher.register_function(
    "Sumar",
    sumar,
    returns={"sumar": str},
    args={"a": int,"b":int},
    
)
dispatcher.register_function(
    "Restar",
    resta,
    returns={"resta": str},
    args={"a": int,"b":int},
    
)
dispatcher.register_function(
    "Multiplicar",
    multiplicacion,
    returns={"multiplicacion": str},
    args={"a": int,"b":int},
    
)

dispatcher.register_function(
    "Dividir",
    division,
    returns={"division": str},
    args={"a": int,"b":int},
    
)


server = HTTPServer(("0.0.0.0", 8000), SOAPHandler)
server.dispatcher = dispatcher
